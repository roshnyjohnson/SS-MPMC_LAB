ASSUME CS:CODE,DS:DATA
DATA SEGMENT
	M1 DB 10,13,"ENTER FIRST$"
	M2 DB 10,13,"ENTER SECOND$"
	M3 DB 10,13,"PRODUCT$"
	PROD DB 4 DUP(0);
DATA ENDS

PRTMSG MACRO MESSAGE
	LEA DX,MESSAGE
	MOV AH,09H
	INT 21H 
ENDM

GETDCM MACRO
	MOV AH,01H
	INT 21H
	SUB AL,30H
ENDM

PRTDCM MACRO
	MOV DL,[SI]
	ADD DL,30H
	MOV AH,02H
	INT 21H
ENDM

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX

	PRTMSG M1
	GETDCM 
	MOV BH,AL
	GETDCM
	MOV BL,AL
	MOV AL,10
	MUL BH
	ADD AL,BL
	MOV BL,AL

	PRTMSG M2
	GETDCM 
	MOV CH,AL
	GETDCM
	MOV CL,AL
	MOV AL,10
	MUL CH
	ADD AL,CL

	MUL BL

	LEA SI,PROD
	ADD SI,3
	MOV CX,4
	MOV BX,10

CONVERT_LOOP:

	MOV DX,0	
	DIV BX
	MOV [SI],DL
	DEC SI
	LOOP CONVERT_LOOP

	LEA SI,PROD
	PRTMSG M3
	PRTDCM
	INC SI
	PRTDCM
	INC SI
	PRTDCM
	INC SI
	PRTDCM

	MOV AH,4CH
	INT 21


	
CODE ENDS
END START


