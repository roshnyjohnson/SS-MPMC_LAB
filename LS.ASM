ASSUME CS:CODE,DS:DATA
DATA SEGMENT
	M1 DB "FOUND$"
	M2 DB "NOT FOUND$"
	STRING DB 11H,22H,33H,44H,55H
	SE DB 11H 
DATA ENDS

PRTMSG MACRO MESSAGE
	LEA DX,MESSAGE
	MOV AH,09H
	INT 21H
ENDM

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX

	MOV AL,SE
	MOV CX,05H
	LEA SI,STRING
	
UP:
	MOV BX,[SI]
	CMP AL,BX
	JZ FOUND

	INC SI
	DEC CX
	JNZ UP

	PRTMSG M2
	JMP DONE

FOUND:
	PRTMSG M1
	
DONE:
	MOV AH,4CH
	INT 21H

CODE ENDS
END START
