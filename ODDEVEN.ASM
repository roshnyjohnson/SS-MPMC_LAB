ASSUME CS:CODE,DS:DATA
DATA SEGMENT
	M1 DB 10,13,"ENTER THE NUMBER$"
	M2 DB 10,13,"ODD$"
	M3 DB 10,13,"EVEN$" 
DATA ENDS

PRTMSG MACRO MESSAGE
	LEA DX,MESSAGE
	MOV AH,09H
	INT 21H

ENDM

GETDCM MACRO
	MOV AH,01H
	INT 21H
	SUB AL,30H	
ENDM

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX

	PRTMSG M1
	GETDCM
	GETDCM

	SHR AL,01
	JC ODD

	PRTMSG M3
	JMP DONE

ODD:
	PRTMSG M2
DONE:
	MOV AH,4CH
	INT 21H
	 
CODE ENDS
END START
